package com.vitindev.jobs.one.calculator;

public class SimpleCalculator {

    public double sum(double x, double z) {
        return x + z;
    }

    public double subtract(double x, double z) {
        return x - z;
    }

    public double multiply(double x, double z) {
        return x * z;
    }

    public double divide(double x, double z) {
        return x / z;
    }

}
